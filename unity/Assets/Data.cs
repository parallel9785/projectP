    public static int StatusCalculation(int i, int j, int k, int l, int m)//(種族,個体,努力値,レベル,回数)
    {
        //努力レベル
        int a = (int)Math.Sqrt(k - 1);
        a = (a + 1) / 4;

        //式
        a = (pokeStatus1[i, m] + j) * 2 + a;
        a = a * l / 100 + 5;

        return a;
    }


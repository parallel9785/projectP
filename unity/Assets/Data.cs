    //HP個体値計算
    public static int HPIV(int A, int B, int S, int C)
    {
        int i = 0;
        if(A % 2 == 1)//攻撃個体値は奇数か
        {
            i += 8;
        }
        if (B % 2 == 1)//防御個体値は奇数か
        {
            i += 4;
        }
        if (S % 2 == 1)//素早さ個体値は奇数か
        {
            i += 2;
        }
        if (C % 2 == 1)//特殊個体値は奇数か
        {
            i += 1;
        }
        return i;
    }
